/**
 * Genera gestione-car-mcq-bank.js da testo incollato (blocchi Q: ... Corretta: X).
 * Uso: node tools/gen-gestione-car-bank.mjs [tools/car-bank-raw.txt]
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const rawPath = process.argv[2] || path.join(root, 'tools', 'car-bank-raw.txt');
const outPath = path.join(root, 'gestione-car-mcq-bank.js');

const raw = fs.readFileSync(rawPath, 'utf8');
const blocks = raw
    .split(/\n-{3,}\n|(?:\r?\n){3,}/)
    .map((b) => b.trim())
    .filter(Boolean);

const items = [];

function parseBlock(block) {
    if (!/^Q\s*:/i.test(block)) return null;
    let rest = block.replace(/^Q\s*:\s*/i, '').trim();

    let risk = '';
    const rMatch = rest.match(/\n\s*Rischio\s+associato\s*:\s*([\s\S]+)$/i);
    if (rMatch) {
        risk = rMatch[1].replace(/\s+/g, ' ').trim();
        rest = rest.slice(0, rMatch.index).trim();
    }

    const corrMatch = rest.match(/\sCorretta\s*:\s*([A-E])\s*\.?\s*$/i);
    if (!corrMatch) return null;
    const letter = corrMatch[1].toUpperCase();
    const head = rest.slice(0, corrMatch.index).trim();

    const optRe = /\s([A-E])\s*:\s*/g;
    const marks = [];
    let m;
    while ((m = optRe.exec(head)) !== null) {
        marks.push({ L: m[1].toUpperCase(), start: m.index, valStart: m.index + m[0].length });
    }
    if (marks.length < 4) return null;

    const qStem = head.slice(0, marks[0].start).trim();
    const answers = [];
    for (let i = 0; i < marks.length; i++) {
        const end = i + 1 < marks.length ? marks[i + 1].start : head.length;
        answers.push(head.slice(marks[i].valStart, end).trim());
    }
    if (answers.length !== 4) return null;

    const idx = letter.charCodeAt(0) - 65;
    if (idx < 0 || idx > 3) return null;

    return {
        q: qStem,
        answers,
        correctIndex: idx,
        ...(risk ? { risk } : {})
    };
}

for (const block of blocks) {
    const parsed = parseBlock(block);
    if (parsed) items.push(parsed);
    else if (/^Q\s*:/i.test(block)) console.warn('Skip:', block.slice(0, 90).replace(/\s+/g, ' ') + '…');
}

const json = JSON.stringify(items);
const out =
    '/* Auto-generated by tools/gen-gestione-car-bank.mjs */\n' +
    'window.GESTIONE_CAR_MCQ_BANK = ' +
    json +
    ';\n';

fs.writeFileSync(outPath, out, 'utf8');
console.log('Wrote', outPath, 'items:', items.length);
